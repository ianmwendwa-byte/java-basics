public class StringLength {
   public static void main(String[] args) {
       String alphabets= "ABCDEFGHIJKLMNOPQRSTUVWXYZ";
       System.out.println("the alphabets length is:"+alphabets.length());
       System.out.println("the alphabets in uppercase is:"+alphabets.toUpperCase());
       System.out.println("the alphabets in lowercase is:"+alphabets.toLowerCase());
    }
    
}
